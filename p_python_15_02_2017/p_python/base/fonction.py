#Magique des fonction
"""
Dans une fonction qu'on ne retourne rien on ne peut pas stocké le resultat de
la fonction par exemple dans une variable,
D'ailleurs si on ne lui demande dans son corps d'afficher un message, quand on
essye de stocker son resultat dans une var elle affiche le resultat sans rien
stocker dans la variable

________________
Avec 'return' non seulement la fonction fait son travail mais elle renvoie
l'élément qu'on lui demande de retourner qu'on peut maintenant prendre soit
dans une variable

"""

def fn(n):
    return n
