"""
---------------------------------------------------------
La programmation orienté-objet (POO)
------------------------------------------


Classe : plan de conception, genre(ex: Humain)
Objet : instance de classe (ex: Julien)

Attribut : Variable de classe (ex : prenom, taille, sexe, age...)
Propriéte : manière de manipuler les attributs (lecture seule, accès non autorisé en déhors de la classe, etc..)


Méthode  : fonction d'une classe (ex: Travailler, écrire, parler, marcher...
Méthode de classe : fonction d'une classe qui agit plus sur la classe
Méthode statique : fonction d'une classe, mais indépendante de celle-ci, peut être utiliser sans extention de la classe


Héritage : Une classe fille qui hérite d'une classe mère
            File est une sorte de mère
            ex Classe Chat hérite de la classe Animal (Chat est une sorte d'Animal)
"""